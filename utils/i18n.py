def normalize_language(value: Any) -> str:
    if value is None:
        return "en"
    raw = str(value).strip()
    if not raw:
        return "en"
    if raw in LANGUAGE_CODES:
        return raw
    # Patient display labels → clinic/session codes when needed.
    patient_label = normalize_patient_language(raw)
    if patient_label in _PATIENT_LABEL_TO_CODE:
        return _PATIENT_LABEL_TO_CODE[patient_label]
    return _LEGACY_LANGUAGE_MAP.get(raw.casefold(), "en")


def normalize_patient_language(value: Any) -> str:
    """Normalize a patient preferred-language value to a display label."""
    if value is None:
        return "English"
    raw = str(value).strip()
    if not raw:
        return "English"
    if raw in PATIENT_LANGUAGE_OPTIONS:
        return raw
    mapped = _PATIENT_LANGUAGE_MAP.get(raw) or _PATIENT_LANGUAGE_MAP.get(raw.casefold())
    return mapped if mapped in PATIENT_LANGUAGE_OPTIONS else "English"


def get_language() -> str:
    return normalize_language(st.session_state.get("ui_language", "en"))


def set_language(language: str) -> None:
    st.session_state.ui_language = normalize_language(language)


def apply_clinician_language(preferred_language: Any) -> None:
    """Load a clinician's saved language preference into the active UI session."""
    st.session_state.pop("preferred_language", None)
    set_language(preferred_language)


def apply_patient_language(preferred_language: Any) -> None:
    """Load a patient's saved language preference into the active UI session."""
    label = normalize_patient_language(preferred_language)
    st.session_state.preferred_language = label
    set_language(_PATIENT_LABEL_TO_CODE.get(label, "en"))


def t(key: str, **kwargs: Any) -> str:
    """Translate a UI string for the active language.

    Patient portal uses ``st.session_state.preferred_language`` display labels
    (English / 简体中文 / Español / 日本語). Clinic portal uses ``ui_language`` codes.
    """
    text = None
    pref = st.session_state.get("preferred_language")
    if isinstance(pref, str) and pref in TRANSLATIONS:
        text = TRANSLATIONS[pref].get(key)
        if text is None:
            text = TRANSLATIONS["English"].get(key)

    if text is None:
        lang = get_language()
        catalog = _STRINGS.get(lang) or _STRINGS["en"]
        text = catalog.get(key) or _STRINGS["en"].get(key) or key

    if kwargs:
        try:
            return text.format(**kwargs)
        except (KeyError, ValueError):
            return text
    return text


def language_label(code: str) -> str:
    code = normalize_language(code)
    for value, label in LANGUAGE_OPTIONS:
        if value == code:
            return label
    return code
