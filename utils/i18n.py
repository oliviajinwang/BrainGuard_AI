# Prefer explicit locale codes; keep legacy "zh" as Simplified Chinese.
_STRINGS["zh-Hans"] = _STRINGS["zh"]
_STRINGS["zh-Hant"] = {key: _to_traditional(value) for key, value in _STRINGS["zh"].items()}


def normalize_language(value: Any) -> str:
    if value is None:
        return "en"
    raw = str(value).strip()
    if not raw:
        return "en"
    if raw in LANGUAGE_CODES:
        return raw
    return _LEGACY_LANGUAGE_MAP.get(raw.casefold(), "en")


def get_language() -> str:
    return normalize_language(st.session_state.get("ui_language", "en"))


def set_language(language: str) -> None:
    st.session_state.ui_language = normalize_language(language)


def apply_clinician_language(preferred_language: Any) -> None:
    """Load a clinician's saved language preference into the active UI session."""
    set_language(preferred_language)


def t(key: str, **kwargs: Any) -> str:
    """Translate a UI string for the active language."""
    lang = get_language()
    catalog = _STRINGS.get(lang) or _STRINGS["en"]
    text = catalog.get(key) or _STRINGS["en"].get(key) or key
    if kwargs:
        try:
            return text.format(**kwargs)
        except (KeyError, ValueError):
            return text
    return text


def language_label(code: str) -> str:
    code = normalize_language(code)
    for value, label in LANGUAGE_OPTIONS:
        if value == code:
            return label
    return code
